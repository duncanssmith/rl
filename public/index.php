<p class="capt">West Weares</p>

<p>
Ranters Lodge is easily reached by public transport from London Waterloo. There is a twice hourly service taking approximately 2 hours 45 minutes to Weymouth. There is also a train service from Bristol to Weymouth. From Weymouth train station there are taxis or a short walk to the king's statue on the esplanade where there is a very regular bus service to Portland (No. 1 bus to Victoria Square.) Then there is a short walk to Ranters Lodge.</p>
<p>
Ranters Lodge is 140 miles (224km) from London and approximately 200 miles (320km) from Dover. There is private parking in front of the buildings.
